// Hitung mundur
const countdownDate = new Date('April 30, 2025 20:30:00').getTime();
const countdownElement = document.getElementById('countdown');
const waitingMessage = document.getElementById('waitingMessage');
const formContainer = document.getElementById('formContainer');

// Mulai hitung mundur
const timer = setInterval(() => {
    const now = new Date().getTime();
    const distance = countdownDate - now;
    
    const days = Math.floor(distance / (1000 * 60 * 60 * 24));
    const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((distance % (1000 * 60)) / 1000);
    
    countdownElement.innerHTML = `
        <div class="countdown-item">
            <div class="countdown-number">${days}</div>
            <div>Hari</div>
        </div>
        <div class="countdown-item">
            <div class="countdown-number">${hours}</div>
            <div>Jam</div>
        </div>
        <div class="countdown-item">
            <div class="countdown-number">${minutes}</div>
            <div>Menit</div>
        </div>
        <div class="countdown-item">
            <div class="countdown-number">${seconds}</div>
            <div>Detik</div>
        </div>
    `;
    
    if(distance < 0) {
        clearInterval(timer);
        countdownElement.innerHTML = "PENGUMUMAN TELAH DIBUKA!";
        waitingMessage.style.display = 'none';
        formContainer.style.display = 'block';
    } else {
        waitingMessage.style.display = 'block';
        formContainer.style.display = 'none';
    }
}, 1000);

// Form handling
document.getElementById('nisnForm').addEventListener('submit', function(e) {
    e.preventDefault();
    const nisn = document.getElementById('nisnInput').value;
    const resultDiv = document.getElementById('result');
    
    if(nisn.length !== 10 || isNaN(nisn)) {
        showError("NISN harus 10 digit angka");
        return;
    }

    if(students[nisn]) {
        const student = students[nisn];
        resultDiv.innerHTML = `
            <h2>HASIL VERIFIKASI</h2>
            <div class="student-info">
                <p><span>NISN:</span> ${nisn}</p>
                <p><span>Nama:</span> ${student.nama}</p>
                <p><span>Konsentrasi Keahlian:</span> ${student.konsentrasi}</p>
                <p><span>Status:</span> <strong>LULUS</strong></p>
            </div>
            <div class="announcement">
                <p>Selamat! Anda telah dinyatakan lulus dari</p>
                <h3>SMKN 1 RENGAT BARAT</h3>
                <p>Tahun Ajaran 2024/2025</p>
            </div>
        `;
        resultDiv.className = 'success';
        resultDiv.style.display = 'block';
    } else {
        showError("NISN tidak terdaftar");
    }
});

function showError(message) {
    const resultDiv = document.getElementById('result');
    resultDiv.innerHTML = `
        <p>${message}</p>
        <p>Silakan verifikasi ke panitia kelulusan</p>
    `;
    resultDiv.className = 'error';
    resultDiv.style.display = 'block';
}